/** スペクトル型文字列(例 "G2V", "M1.5Iab") → 日本語の短い説明 */
export function spectralDescription(spect: string): string {
  if (!spect) return "—";

  // 白色矮星
  if (spect.startsWith("D")) return "白色矮星";
  // ウォルフ・ライエ星
  if (spect.startsWith("W")) return "ウォルフ・ライエ星（超高温の輝線星）";

  const typeChar = spect[0].toUpperCase();

  // 光度階級を判定
  let luminosity = "";
  if (/I[ab]|Ia|Ib|I(?![IVab])/.test(spect)) {
    luminosity = "超巨星";
  } else if (/II(?!I)/.test(spect)) {
    luminosity = "輝巨星";
  } else if (/III(?!I)/.test(spect)) {
    luminosity = "巨星";
  } else if (/IV/.test(spect)) {
    luminosity = "準巨星";
  } else if (/V/.test(spect)) {
    luminosity = "主系列星";
  }

  const suffix = luminosity ? `（${luminosity}）` : "";

  switch (typeChar) {
    case "O":
      return `青色の超高温星（30,000K以上）${suffix}`;
    case "B":
      return `青白色の高温星（10,000〜30,000K）${suffix}`;
    case "A":
      return `白色の星（7,500〜10,000K）${suffix}`;
    case "F":
      return `黄白色の星（6,000〜7,500K）${suffix}`;
    case "G":
      return `太陽に似た黄色い星（5,200〜6,000K）${suffix}`;
    case "K":
      return `橙色の星（3,700〜5,200K）${suffix}`;
    case "M":
      return `赤色の低温星（3,700K以下）${suffix}`;
    case "L":
      return `褐色矮星（極低温の暗天体）`;
    case "T":
      return `メタン褐色矮星`;
    default:
      return "—";
  }
}
